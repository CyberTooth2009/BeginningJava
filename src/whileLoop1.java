import java.util.Scanner;
public class whileLoop1 {
    public static <string> void main(String[] args) {
        System.out.println("This program prints the first 10 numbers in list form using while loops:");
        //Assign variables
        int N = 0;
        //Loop for printing the numbers
        while(N<10) {
            //Print the values
            N++;
            System.out.println(N);
        }
    }
}
