public class forloop {
    public static <string> void main(String[] args) {
        System.out.println("This program prints the first 10 numbers in list form:");
        //Assign the values of N to 0-10
        for (int N = 0; N <= 10; N++) {
//Print the values
            System.out.println(N);
        }
    }
}
