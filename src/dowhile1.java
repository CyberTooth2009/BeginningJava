import java.util.Scanner;
public class dowhile1 {
    public static <string> void main(String[] args) {
        System.out.println("This program prints the first 10 numbers in list form using do-while loops:");
        //Assign variables
        int N = 0;
        //Loop for printing the numbers
       do{
            //Print the values
            N++;
            System.out.println(N);
        } while(N<10);
    }
}
