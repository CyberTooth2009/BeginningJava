public class forLoop {
    public static void main(String[] args) {
        //Declare variables
        int sum = 0;

        //Print out natural numbers
        System.out.println("The first 10 natural numbers are: ");
        for(int i = 0;i<=10;i++){

            sum = sum + i;

            System.out.println(i);
        }




    }
}
