public class doWhileLoops {
    public static void main(String[] args) {
        //Define variables
        int counter = 1;

        //Run do-while loop
        System.out.println("The first 10 natural numbers are: \n");


        do {
            System.out.println(counter);
            counter++;
        }
        while(counter <= 10);
        }
    }

