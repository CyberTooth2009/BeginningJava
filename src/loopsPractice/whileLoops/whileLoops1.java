public class whileLoops1 {
    public static void main(String[] args) {
        //Define variables
        int counter = 1;

        //Run while loop
        System.out.println("The first 10 natural numbers are: ");
        while(counter <= 10){
            System.out.println(counter);
            counter++;
        }








    }
}
